package inheritance;

public class Faculty extends Person {
	
	private double salary;

	public Faculty(String name, double salary) {
		super(name);
		this.salary = salary;
	}	
	
	public double getSalary() {
		return salary;
	}

	public void setSalary(double salary) {
		this.salary = salary;
	}

	@Override
	public String toString() {
		//return "Faculty [toString()=" + super.toString() + ", salary=" + salary + "]";
		return /**super.toString()**/ "Name: " + getName() + " ID: " + getId() + " Salary: " + salary;
	}
}
